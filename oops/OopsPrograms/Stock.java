package OopsPrograms;

public class Stock
{

	 private String companyName;
     private int noOfShares;
     private long eachSharePrice;
     private int totalstockCount;
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public int getNoOfShares() {
		return noOfShares;
	}
	public void setNoOfShares(int noOfShares) {
		this.noOfShares = noOfShares;
	}
	public long getEachSharePrice() {
		return eachSharePrice;
	}
	public void setEachSharePrice(long eachSharePrice) {
		this.eachSharePrice = eachSharePrice;
	}
	public int getTotalstockCount() {
		return totalstockCount;
	}
	public void setTotalstockCount(int totalstockCount) {
		this.totalstockCount = totalstockCount;
	}
     

}
